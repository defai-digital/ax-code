// ADR-046 Slice A: generate crates/ax-code-render/src/gcb_table.rs — the
// per-codepoint 20-class grapheme-break property table matching uucode
// (the unicode library inside the shipped Zig binary; Unicode 16.0.0 UCD).
//
// Derivation precedence transliterated from uucode src/build/tables.zig:
//   Emoji_Modifier > Emoji_Modifier_Base > Extended_Pictographic >
//   InCB(extend -> zwj for U+200D else incb_extend; linker; consonant) >
//   original GCB (Extend + InCB=none happens only for U+200C -> zwnj).
//
// Inputs are the UCD files vendored from the uucode commit that upstream
// opentui pins (ax-internal/reference/uucode-84ceda8/ucd/). Regenerate only
// when the pinned @opentui/core version (and thus its uucode) changes.
//
// Run with: node script/gen-native-render-gcb-table.mjs

import { readFileSync, writeFileSync } from "node:fs"
import path from "node:path"
import { fileURLToPath } from "node:url"

const here = path.dirname(fileURLToPath(import.meta.url))
const ucdDir = path.resolve(here, "../../../ax-internal/reference/uucode-84ceda8/ucd")
const out = path.resolve(here, "../../../crates/ax-code-render/src/gcb_table.rs")

const CLASSES = [
  "other", "control", "prepend", "cr", "lf", "regional_indicator", "spacing_mark",
  "l", "v", "t", "lv", "lvt", "zwj", "zwnj", "extended_pictographic",
  "emoji_modifier_base", "emoji_modifier", "incb_extend", "incb_linker", "incb_consonant",
]
const id = Object.fromEntries(CLASSES.map((c, i) => [c, i]))

function parseUcd(file, wantProp) {
  const ranges = []
  for (const raw of readFileSync(file, "utf8").split("\n")) {
    const line = raw.split("#")[0].trim()
    if (!line) continue
    const [cps, ...props] = line.split(";").map((s) => s.trim())
    const prop = props.join(";")
    if (wantProp && prop !== wantProp) continue
    const [lo, hi] = cps.split("..").map((s) => parseInt(s, 16))
    ranges.push([lo, hi ?? lo, prop])
  }
  return ranges
}

const N = 0x110000
const gcb = new Uint8Array(N) // original GCB, mapped later
const GCB_MAP = {
  Prepend: "prepend", CR: "cr", LF: "lf", Control: "control", Extend: "extend",
  Regional_Indicator: "regional_indicator", SpacingMark: "spacing_mark",
  L: "l", V: "v", T: "t", LV: "lv", LVT: "lvt", ZWJ: "zwj",
}
const ORIG = ["other", "prepend", "cr", "lf", "control", "extend", "regional_indicator", "spacing_mark", "l", "v", "t", "lv", "lvt", "zwj"]
const origId = Object.fromEntries(ORIG.map((c, i) => [c, i]))
for (const [lo, hi, prop] of parseUcd(path.join(ucdDir, "GraphemeBreakProperty.txt"))) {
  const mapped = GCB_MAP[prop]
  if (!mapped) continue
  for (let cp = lo; cp <= hi; cp++) gcb[cp] = origId[mapped]
}

const extPict = new Uint8Array(N)
const emojiMod = new Uint8Array(N)
const emojiModBase = new Uint8Array(N)
for (const [lo, hi, prop] of parseUcd(path.join(ucdDir, "emoji-data.txt"))) {
  const target = prop === "Extended_Pictographic" ? extPict : prop === "Emoji_Modifier" ? emojiMod : prop === "Emoji_Modifier_Base" ? emojiModBase : null
  if (!target) continue
  for (let cp = lo; cp <= hi; cp++) target[cp] = 1
}

const incb = new Uint8Array(N) // 0 none, 1 extend, 2 linker, 3 consonant
for (const [lo, hi, prop] of parseUcd(path.join(ucdDir, "DerivedCoreProperties.txt"))) {
  const m = prop.match(/^InCB;\s*(\w+)$/)
  if (!m) continue
  const v = { Extend: 1, Linker: 2, Consonant: 3 }[m[1]]
  for (let cp = lo; cp <= hi; cp++) incb[cp] = v
}

const table = new Uint8Array(N)
let warnings = 0
for (let cp = 0; cp < N; cp++) {
  let cls
  if (emojiMod[cp]) cls = id.emoji_modifier
  else if (emojiModBase[cp]) cls = id.emoji_modifier_base
  else if (extPict[cp]) cls = id.extended_pictographic
  else if (incb[cp] === 1) cls = cp === 0x200d ? id.zwj : id.incb_extend
  else if (incb[cp] === 2) cls = id.incb_linker
  else if (incb[cp] === 3) cls = id.incb_consonant
  else {
    const orig = ORIG[gcb[cp]]
    if (orig === "extend") {
      if (cp === 0x200c) cls = id.zwnj
      else {
        // uucode asserts this is unreachable in Unicode 16 data
        if (++warnings < 5) console.warn(`Extend with InCB=none: U+${cp.toString(16)}`)
        cls = id.incb_extend
      }
    } else cls = id[orig]
  }
  table[cp] = cls
}

// Range-compress (class 0 = other is the default).
const ranges = []
for (let cp = 0; cp < N; cp++) {
  if (table[cp] === 0) continue
  const last = ranges[ranges.length - 1]
  if (last && last[1] === cp - 1 && last[2] === table[cp]) last[1] = cp
  else ranges.push([cp, cp, table[cp]])
}
console.log(`gcb ranges: ${ranges.length}${warnings ? `, warnings: ${warnings}` : ""}`)

writeFileSync(
  out,
  `//! GENERATED by packages/ax-code/script/gen-native-render-gcb-table.mjs from
//! the Unicode 16.0.0 UCD files vendored by the uucode commit that upstream
//! opentui pins. Do not edit by hand. Class ids match unicode::gcb::Gcb.

pub const GCB_RANGES: &[(u32, u32, u8)] = &[
${ranges.map(([a, b, c]) => `    (0x${a.toString(16).toUpperCase()}, 0x${b.toString(16).toUpperCase()}, ${c}),`).join("\n")}
];
`,
)
console.log(`wrote ${out}`)
