// ADR-046 Slice A: generate crates/ax-code-render/src/width_table.rs by
// probing the shipped Zig library's encodeUnicode for every codepoint.
// This bakes the exact per-codepoint cell width of the reference binary
// (East_Asian_Width + explicit wide ranges + zero-width marks/format chars)
// into a Rust range table — parity by construction, no UCD version guessing.
//
// Run with: node --experimental-ffi script/gen-native-render-width-table.mjs
// Regenerate only when the pinned @opentui/core-<platform> version changes.

import { writeFileSync } from "node:fs"
import path from "node:path"
import { fileURLToPath } from "node:url"
import { resolveRenderLib } from "@ax-code/opentui-core"

const lib = resolveRenderLib()
const out = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "../../../crates/ax-code-render/src/width_table.rs")

// Per-codepoint width = the Zig eawToWidth clamped to >= 0. Extracted via
// wcwidth mode (cluster width = SUM of per-codepoint widths, no VS16/RI
// cluster rules) with a leading space anchor: whether or not the probed
// codepoint joins the space's grapheme cluster, sum(entry widths) - 1 is its
// width. A bare single-codepoint probe is NOT usable: leading combining marks
// take a per-byte fallback path in the Zig encoder and emit junk cells.
function probe(cp) {
  const encoded = lib.encodeUnicode(" " + String.fromCodePoint(cp), "wcwidth")
  if (!encoded) return 0
  let sum = 0
  for (const cell of encoded.data) sum += cell.width
  if (encoded.data.length > 0) lib.freeUnicode(encoded)
  return Math.max(0, sum - 1)
}

const start = 0x80 // below 0x80 is handled by the transliterated charWidth
const end = 0x110000
const widths = new Uint8Array(end)
let probed = 0
for (let cp = start; cp < end; cp++) {
  if (cp >= 0xd800 && cp <= 0xdfff) continue // surrogates: unreachable in UTF-8
  widths[cp] = probe(cp)
  probed++
  if (probed % 200000 === 0) console.log(`probed ${probed} codepoints…`)
}

// Compress to ranges of width 0 and width 2 (default is 1).
function ranges(target) {
  const list = []
  let runStart = -1
  for (let cp = start; cp <= end; cp++) {
    const match = cp < end && !(cp >= 0xd800 && cp <= 0xdfff) && widths[cp] === target
    if (match && runStart < 0) runStart = cp
    if (!match && runStart >= 0) {
      list.push([runStart, cp - 1])
      runStart = -1
    }
  }
  return list
}
const zero = ranges(0)
const wide = ranges(2)
console.log(`width-0 ranges: ${zero.length}, width-2 ranges: ${wide.length}`)

const fmt = (list) => list.map(([a, b]) => `    (0x${a.toString(16).toUpperCase()}, 0x${b.toString(16).toUpperCase()}),`).join("\n")
writeFileSync(
  out,
  `//! GENERATED by packages/ax-code/script/gen-native-render-width-table.mjs —
//! per-codepoint cell widths probed from the shipped Zig library
//! (@opentui/core 0.4.1 encodeUnicode). Do not edit by hand; regenerate when
//! the pinned native version changes. Codepoints below 0x80 are handled in
//! code; everything not listed here is width 1.

pub const ZERO_WIDTH_RANGES: &[(u32, u32)] = &[
${fmt(zero)}
];

pub const WIDE_RANGES: &[(u32, u32)] = &[
${fmt(wide)}
];
`,
)
console.log(`wrote ${out}`)
process.exit(0)
